	public void setPoint(LocalDataPoint point)
	{
		m_point = point;
		
		if (m_point != null)
		{
			txtName.setText(m_point.getName());

			Object obj = m_point.getValue();

			if (obj != null)
				txtValue.setText(m_point.getValue().toString());

			chbReadOnly.setSelected(m_point.isReadOnly());
			cmbType.setSelectedItem(m_point.DataType.toString().substring(3));
		}
	}

