private void btnOkMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnOkMouseClicked
	// apply the values
	if (m_point != null)
	{
		m_point.setName(txtName.getText());
		
		if (chbReadOnly.isSelected())
			m_point.Attributes = 0x01;
		
		int dt = cmbType.getSelectedIndex();
		
		m_point.DataType = DataTypeEnum.valueOf((byte) dt);
		m_point.setValue(txtValue.getText());
	}
	setVisible(false);
}//GEN-LAST:event_btnOkMouseClicked

